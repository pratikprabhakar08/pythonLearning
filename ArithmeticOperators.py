# Examples of Arithmetic Operator  
a = 10
b = 2
  
# Addition 
add = a + b  
  
# Subtraction
sub = a - b  
  
# Multiplication 
mul = a * b  
  
# Division with decimal
div1 = a / b  
  
# Division with floor 
div2 = a // b  
  
# Modulo
mod = a % b  
  
# Power 
p = a ** b 
  
# results  
print(add)  
print(sub)  
print(mul)  
print(div1)  
print(div2)  
print(mod) 
print(p) 
